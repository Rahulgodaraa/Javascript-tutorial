/// DATA TYPES IN JAVA SCRIPT 

/* NUMBERS */

let numb = 50;
console.log(numb);
console.log(typeof numb);

/* STRINGS */

let str = "Rahul is a string";
console.log(str);
console.log(typeof str);

/* BOOLEAN */

let prob = true;
console.log(prob);
console.log(typeof prob);

/* DYNAMIC TYPING */

let Rahulisjaat = true;
console.log(typeof Rahulisjaat);

/* Changing the type 
   dont use let,const,etc */

Rahulisjaat = "yes he is a jaat";
console.log(typeof Rahulisjaat);

/* UNDEFINED */

let notdefined;
console.log(typeof notdefined);
